hydrogen = elements.symbol('H')
print(hydrogen.mass)

sulfur = elements.symbol('S')
print(sulfur.mass)

oxygen = elements.symbol('O')
print(oxygen.mass)

nitrogen = elements.symbol('N')
print(nitrogen.mass)